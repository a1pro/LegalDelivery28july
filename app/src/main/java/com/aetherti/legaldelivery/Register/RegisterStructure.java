package com.aetherti.legaldelivery.Register;

public class RegisterStructure {
	
	private String DeviceID;
	private String DeviceCode;
	private String DeviceName;
	private String DeviceUserId;
	private String DeviceUserPwd;

	public String getDeviceUserPwd() {
		return DeviceUserPwd;
	}
	public void setDeviceUserPwd(String deviceUserPwd) {
		DeviceUserPwd = deviceUserPwd;
	}
	public String getDeviceID() {
		return DeviceID;
	}
	public void setDeviceID(String deviceID) {
		DeviceID = deviceID;
	}
	public String getDeviceCode() {
		return DeviceCode;
	}
	public void setDeviceCode(String deviceCode) {
		DeviceCode = deviceCode;
	}
	public String getDeviceName() {
		return DeviceName;
	}
	public void setDeviceName(String deviceName) {
		DeviceName = deviceName;
	}
	public String getDeviceUserId() {
		return DeviceUserId;
	}
	public void setDeviceUserId(String deviceUserId) {
		DeviceUserId = deviceUserId;
	}
	
	
	
	
	

}
